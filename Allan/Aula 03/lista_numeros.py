##Gera uma sequência de números##

"""contar o numero sempre colocando um numero a mais """
for numero in range(11):
    print(numero)


"""contar de 0 a 10 """
for numero in range(0,11): ##contando de 0 a 10##
        print(numero)


"""contar em 2 em 2 """
for numero in range(0,11,2):
     print(numero)

